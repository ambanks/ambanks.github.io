// Options
var scrollSpeed = 750;		// Speed of scrolling in ms
var topOffset = 80;			// Offset in px

// Scroll Buttons
var scrollButtons = $('#nav a, #portfolio, .request');	// Scroll Buttons (ID or Class)

// Functions
$(scrollButtons).on('click', function() {

    var scrollAnchor = $(this).attr('data-scroll'),
        scrollPoint = $('section[data-anchor="' + scrollAnchor + '"]').offset().top - topOffset;

    $('body, html').animate({
        scrollTop: scrollPoint
    }, scrollSpeed);

    return false;

});


$(window).scroll(function() {
    var windscroll = $(window).scrollTop();
    var welcomeHeight = $('.welcome').height();

    if (windscroll >= welcomeHeight ) {
        $('.header').addClass('fixed');
        $('.welcome').css({'margin-top' : 80});

        $('body > section').each(function(i) {
            if ($(this).position().top - 1 <= windscroll - $(this).height() + 80) {
                $('#nav a.active').removeClass('active');
                $('#nav a').eq(i).addClass('active');
            }
        });

    } else {

        $('.header').removeClass('fixed');
        $('.welcome').css({'margin-top' : 0});
        $('#nav a.active').removeClass('active');
        $('#nav a:first').addClass('active');
    }

}).scroll();

